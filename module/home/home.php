          <p style="font-weight:bold;"><u>संस्थान का इतिहास</u></p>
<p>राजस्थान के मरुभूमि नाम से प्रसिद्ध बीकानेर का गौरव पूर्ण इतिहास रहा है | शिक्षक प्रशिक्षण  के लिए इस संस्थान की  स्थापना 1940 में की गई | 1946 में शिक्षक प्रशिक्षण प्रमाण पत्र पाठ्यक्र्म हेतु स्वीकृति मिली | 1956 में संस्थान को बी.एड. स्तर की मान्यता देते हुए शिक्षक प्रशिक्षण महाविद्यालय में क्र्मोन्न्त किया गया | संस्थान को 1970 में एम.एड. स्तर की मान्यता प्राप्त हुई, इससे संस्थान को प्रथम स्नातकोत्तर शिक्षा महाविद्यालय होने का गौरव प्राप्त हुआ | 1974 में इसे ग्रीष्मकालीन सेवारत शिक्षक प्रशिक्षण का केंद्र बनाया गया | विद्यालयी शिक्षा एवं शिक्षण में गुणात्मक उन्नयन हेतु शैक्षिक नेतृत्व देने के उद्देश्य से केंद्र सरकार की स्वीकृति के पश्चात राज्य सरकार ने सितंबर 1988 में संस्थान को ‘राजकीय उच्च अध्ययन शिक्षा संस्थान (IASE) के रूप में स्तरोन्नत किया |
राष्ट्रीय शिक्षा नीति 1986 के तहत शिक्षकों के व्यावसायिक उन्नयन एवं शैक्षिक गुणवत्ता सवर्धन हेतु सेवारत प्रशिक्षण पर बल दिया गया | जिसके तहत सम्पूर्ण देश में उच्च अध्ययन शिक्षा संस्थान एवं शिक्षक शिक्षा महाविद्यालयों की स्थापना की गयी | इसी क्रम में बीकानेर में यह संस्थान राजस्थान का प्रथम उच्च अध्ययन शिक्षा संस्थान बना | वर्तमान में राजस्थान में दो उच्च अध्ययन शिक्षा संस्थान (बीकानेर एवं अजमेर) एवं आठ शिक्षक शिक्षा महाविद्यालय है |
आजादी से पूर्व स्थापित यह संस्थान प्रदेश में शैक्षिक उन्नयन एवं शिक्षा प्रसार हेतु समर्पित है |</p>

<p style="font-weight:bold;"><u>संस्थान के उद्देश्य</u></p>
<p>उच्च अध्ययन शिक्षा संस्थान की स्थापना निम्नकित उद्देश्यों के लिए की गयी है –<BR />
1.	बी.एड. एवं एम. एड. सेवा पूर्व पाठ्यक्रम आयोजित कर माध्यमिक शिक्षा के लिए प्रशिक्षित शिक्षक तैयार करना|<BR />
2.	सेवारत शिक्षकों के व्यावसायिक उन्नयन एवं शैक्षिक गुणवत्ता सुधार हेतु सेवारत प्रशिक्षण कार्यक्र्म आयोजित करना |<BR />
3.	माध्यमिक एवं उच्च माध्यमिक विद्यालयों के संस्थाप्रधानों मंस नेतृत्व एवं प्रशासनिक क्षमता संवर्धन हेतु सेवारत प्रशिक्षण आयोजित करना |<BR />
4.	माध्यमिक शिक्षा में शैक्षिक नवाचारों हेतु शिक्षकों को जागरूक करना |<BR />
5.	माध्यमिक शिक्षा हेतु महत्वपूर्ण विषयों पर शैक्षिक शोध सम्पन्न करना |<BR />
6.	शैक्षिक मूल्यों, पर्यावरण संवर्द्धन, शैक्षिक प्रोधोगिकी, जनसंख्या शिक्षा, कंप्यूटर शिक्षा, व्यावसायिक शिक्षा, आपदा प्रबंधन, नारी सशक्तीकरण आदि विषयों में सेवारत प्रशिक्षण आयोजित करना |<BR />
7.	विभिन्न शिक्षा संस्थानों में आयोज्य प्रशिक्षण कार्यक्रमों के लिए संदर्भ व्यक्ति प्रशिक्षणों का आयोजन करना |<BR />
8.	कार्यरत शिक्षकों को कार्यरत स्थल पर ‘On Site Support’ प्रदान करना |<BR />
9.	विभिन्न शिक्षा संस्थानों में समन्वय स्थापित कर शैक्षिक अनुभवों का शिक्षक शिक्षा में उपयोग |
</p>
          
